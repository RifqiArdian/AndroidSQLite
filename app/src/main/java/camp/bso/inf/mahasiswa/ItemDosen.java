public class ItemDosen {
    private int mId;
    private String Nip;
    private String Nama;

    public ItemDosen() {}


    public int getmId() {

        return mId;
    }

    public void setmId(int mId) {

        this.mId = mId;
    }

    public String getNip() {

        return Nip;
    }

    public void setNip(String Nip) {
        this.Nip = Nip;
    }

    public String getNama() {

        return Nama;
    }

    public void setNama(
            String Nama) {
        this.Nama = Nama;
    }
}
